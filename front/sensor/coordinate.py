class Coordinate:
    x = 0
    y = 0
    z = 0

    def __init__(self):
        x = 0
        y = 0
        z = 0
    def setCoordinate(tempX, tempY, tempZ):
        x = tempX
        y = tempY
        z = tempZ
    def getX():
        return x
    def getY():
        return y
    def getZ():
        return z
		